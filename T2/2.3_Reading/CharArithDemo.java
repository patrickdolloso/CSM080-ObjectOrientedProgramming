// Character variables can be handled like integers
class CharArithDemo {
  public static void main(String args[]) {
    char ch;
    ch = 'X';
    System.out.println("Character: " + ch);

    ch++;
    System.out.println("Character: " + ch);  

    ch = 90;
    System.out.println("Character: " + ch);
  }
}