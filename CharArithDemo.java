class CharArithDemo {
  public static void main(String args[]) {
    char ch;
    ch = 'X';
    System.out.println("ch contains " + ch);

    ch++; // increment ch to next character, Y in ASCII/unicode
    System.out.println("ch is now " + ch);

    ch = 90; // give ch the value 90 in ASCII unicode
    System.out.println("ch is now " + ch);
  }
}